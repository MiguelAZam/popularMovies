package com.example.android.popularmovies.models;

public class Video {

    private String id;
    private String key;
    private String name;
    private String site;
    private String type;

    public String getId() {
        return id;
    }

    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }

    public String getSite() {
        return site;
    }

    public String getType(){
        return type;
    }
}
